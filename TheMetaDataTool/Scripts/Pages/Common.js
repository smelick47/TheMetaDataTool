    handeler.parseSPException = function (exception) {
        var e = "";
        var m = "";

        try {
            m = exception.get_message();
            e = exception.get_errorTypeName();
        } catch (ex) {
            e = "General";
        };

        switch (e) {
            case "System.UnauthorizedAccessException":
                return defs.Error_UnauthorizedAccess;
            case "Microsoft.SharePoint.Taxonomy.TermStoreOperationException":
                return m;
            case "System.ArgumentException":
                return m;
            default:
                return defs.Error_General;
        };
    };

